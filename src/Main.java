public class Main {
    public static void main(String[] args) {
        // integer, byte (8 bits, 2^8), short (16 bits, 0 to 2^16 ), int (32), long (64)
        // decimal, float (4 byte), double (8 byte)
        Double d = 18.90;
        Float f = 12.34f;
        System.out.println(12.34f * 12.56);
        // char, 1 byte 'a', 'b'
        char letter = 'a';
        // boolean, true, false
        boolean b = false;
        b = true;
    }
}
